"""breath_tone — continuous guide tone for breath.py (zero-dependency).

Synthesizes a per-phase guide WAV (additive resynthesis of a recorded
breathing-guide hum; design 'ref_hum2' from arc 260606a) and plays it with
afplay at each phase start. Pitch contour tells you where you are in the
phase; the optional 'ticks' variant adds a faint blip each second.

  inhale — soft hum alternating between G3 (~197 Hz) and B3 (~247 Hz),
           arched loudness (peaks ~2/3 in)
  exhale — low drone (32.75 Hz harmonic series, measured from reference #2);
           drone only, no breath-noise tail (user round 8)

Amplitude tables are measured from the source recordings at 50 ms hops and
time-stretched to the configured phase durations, so any --ratio works.
The exhale's source cycle is preserved at docs/tone_reference.wav (10.5 s,
one full guide cycle). Synthesized files are cached under
~/.cache/breath_tones/.

macOS only (afplay); breath.py falls back to its chime cues elsewhere.
"""

import array
import io
import math
import os
import subprocess
import sys
import wave

SAMPLE_RATE = 44100
PEAK = 0.45             # normalisation ceiling
FADE_S = 0.15           # raised-cosine edge fade
TICK_AMP, TICK_DUR = 0.07, 0.13
TICK_F_IN, TICK_F_OUT = 495.0, 262.0
AFPLAY = '/usr/bin/afplay'
CACHE_DIR = os.path.expanduser('~/.cache/breath_tones')
TONE_VERSION = 'r9'    # bump to invalidate cached WAVs


def _apply_edge_fades(buf, sr, fade_s=FADE_S):
    n = len(buf)
    fn = min(int(fade_s * sr), n // 2)
    for i in range(fn):
        g = 0.5 - 0.5 * math.cos(math.pi * i / fn)
        buf[i] *= g
        buf[n - 1 - i] *= g
    return buf


def _add_ticks(buf, d, sr, inhale):
    """Faint blip at each interior integer second (the 'ticks' variant)."""
    f = TICK_F_IN if inhale else TICK_F_OUT
    tick_n = int(TICK_DUR * sr)
    for sec in range(1, int(math.ceil(d))):
        start = int(sec * sr)
        if start + tick_n > len(buf):
            break
        for i in range(tick_n):
            env = 0.5 - 0.5 * math.cos(2 * math.pi * i / tick_n)
            t = i / sr
            buf[start + i] += TICK_AMP * env * (
                math.sin(2 * math.pi * f * t)
                + 0.35 * math.sin(2 * math.pi * 2 * f * t))
    return buf


def _resynth(freqs, amp_table, d, sr):
    """Additive resynthesis: fixed partials, amplitudes linearly interpolated
    from the measured 50ms-hop table, time-stretched to duration d."""
    n = int(round(d * sr))
    nfr = len(amp_table)
    buf = array.array('d', bytes(8 * n))
    ph = [0.0] * len(freqs)
    for i in range(n):
        u = (i / (n - 1)) * (nfr - 1) if n > 1 else 0.0
        k = min(int(u), nfr - 2)
        w = u - k
        s = 0.0
        for j, f in enumerate(freqs):
            ph[j] += 2 * math.pi * f / sr
            a = amp_table[k][j] * (1 - w) + amp_table[k + 1][j] * w
            s += a * math.sin(ph[j])
        buf[i] = s
    return _apply_edge_fades(buf, sr)


def _normalise(buf, peak=PEAK):
    m = max(abs(v) for v in buf) or 1.0
    k = peak / m
    for i in range(len(buf)):
        buf[i] *= k
    return buf


def _to_wav_bytes(buf, sr):
    pcm = array.array('h', (int(max(-1.0, min(1.0, v)) * 32767) for v in buf))
    bio = io.BytesIO()
    with wave.open(bio, 'wb') as w:
        w.setnchannels(1)
        w.setsampwidth(2)
        w.setframerate(sr)
        w.writeframes(pcm.tobytes())
    return bio.getvalue()


def synth_phase_wav(inhale, duration, ticks=False, sr=SAMPLE_RATE):
    """Synthesize one phase; returns WAV file bytes (16-bit mono)."""
    if inhale:
        buf = _resynth(REF_IN_PARTIALS, REF_IN_AMPS, duration, sr)
    else:
        freqs = [REF2_DRONE_F0 * h
                 for h in range(1, len(REF2_DRONE_HARMS[0]) + 1)]
        buf = _resynth(freqs, REF2_DRONE_HARMS, duration, sr)
    _normalise(buf)
    if ticks:
        _add_ticks(buf, duration, sr, inhale)
    return _to_wav_bytes(buf, sr)


def _cache_path(inhale, duration, ticks):
    name = 'ref_hum2{}_{}_{:.2f}_{}.wav'.format(
        '_ticks' if ticks else '', 'in' if inhale else 'out',
        duration, TONE_VERSION)
    return os.path.join(CACHE_DIR, name)


def prepare_wavs(inhale_s, exhale_s, ticks=False, quiet=False):
    """Synthesize (or reuse cached) per-phase WAVs; returns {phase: path}."""
    os.makedirs(CACHE_DIR, exist_ok=True)
    paths = {}
    for inhale, dur in ((True, inhale_s), (False, exhale_s)):
        p = _cache_path(inhale, float(dur), ticks)
        if not os.path.isfile(p) or os.path.getsize(p) == 0:
            if not quiet:
                sys.stderr.write('synthesizing guide tone ({})...\n'.format(
                    'inhale' if inhale else 'exhale'))
            data = synth_phase_wav(inhale, float(dur), ticks)
            tmp = p + '.tmp'
            with open(tmp, 'wb') as f:
                f.write(data)
            os.replace(tmp, p)
        paths[inhale] = p
    return paths


class TonePlayer:
    """Plays the per-phase guide WAV via afplay; one process at a time.

    Contract with breath.py: start(inhale) at each phase start (kills any
    previous playback), stop() on pause / mute / quit. After an unmute or
    resume, silence lasts until the next phase start — phases always begin
    aligned.
    """

    def __init__(self, inhale_s, exhale_s, ticks=False, volume=0.25,
                 quiet=False):
        if not (os.path.isfile(AFPLAY) and os.access(AFPLAY, os.X_OK)):
            raise RuntimeError('afplay not available')
        self.volume = volume
        self.paths = prepare_wavs(inhale_s, exhale_s, ticks, quiet)
        self.proc = None

    def start(self, inhale):
        self.stop()
        try:
            self.proc = subprocess.Popen(
                [AFPLAY, '-v', str(self.volume), self.paths[inhale]],
                stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        except OSError:
            self.proc = None

    def stop(self):
        if self.proc is not None and self.proc.poll() is None:
            try:
                self.proc.terminate()
            except OSError:
                pass
        self.proc = None




